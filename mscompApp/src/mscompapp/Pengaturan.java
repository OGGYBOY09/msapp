    private void btnSimpanconfigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanconfigActionPerformed
        // TODO add your handling code here:
        Properties prop = new Properties();
    try (FileOutputStream fos = new FileOutputStream(CONFIG_FILE)) {
        prop.setProperty("db.url", tfUrl.getText().trim());
        prop.setProperty("db.user", tfUser.getText().trim());
        prop.setProperty("db.pass", tfPassword.getText());
        prop.store(fos, "Database Connection Configuration");

        JOptionPane.showMessageDialog(this, "Konfigurasi disimpan! Aplikasi akan restart otomatis.");
        
        // Panggil method restart yang kita buat tadi
        restartApplication();
        
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanconfigActionPerformed

    private void btnSimpanprofilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanprofilActionPerformed
        // TODO add your handling code here:
        try {
            // Validasi Input
            if (tfNama.getText().isEmpty() || tfUsr.getText().isEmpty() || tfPass.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama, Username, dan Password tidak boleh kosong!");
                return;
            }

            Connection conn = Koneksi.configDB();
            String sql = "UPDATE tbl_user SET nama=?, username=?, password=? WHERE id_user=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, tfNama.getText());
            ps.setString(2, tfUsr.getText());
            ps.setString(3, tfPass.getText());
            ps.setString(4, tfNo.getText());

            int result = ps.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Profil berhasil diperbarui!");
                // Opsional: Update label nama di Dashboard utama jika ada
                Login.namaUser = tfNama.getText(); 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal update profil: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanprofilActionPerformed


