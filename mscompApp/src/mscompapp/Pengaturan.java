    private void loadDataConfig() {
        Properties prop = new Properties();
        try (FileInputStream fis = new FileInputStream(CONFIG_FILE)) {
            prop.load(fis);
            tfUrl.setText(prop.getProperty("db.url", "jdbc:mysql://localhost:3306/ms_db"));
            tfUser.setText(prop.getProperty("db.user", "root"));
            tfPassword.setText(prop.getProperty("db.pass", ""));
        } catch (IOException e) {
            tfUrl.setText("jdbc:mysql://localhost:3306/ms_db");
            tfUser.setText("root");
            tfPassword.setText("");
        }
    }
    
    private void loadUserProfile() {
        try {
            Connection conn = Koneksi.configDB();
            // Ambil data berdasarkan Session idUser yang sedang login
            String sql = "SELECT * FROM tbl_user WHERE id_user = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, Session.idUser); 
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                tfNo.setText(rs.getString("id_user"));
                tfRole.setText(rs.getString("role"));
                tfNama.setText(rs.getString("nama"));
                tfUsr.setText(rs.getString("username"));
                tfPass.setText(rs.getString("password"));
            }
        } catch (Exception e) {
            System.err.println("Gagal load profil: " + e.getMessage());
        }
    }

